package com.xinyuegu.star.knowledge.model;

/**
 * @author hxy
 * @time 2018/1/4
 * @desc
 */
public class KnowledgeClassification {
    private Integer id;
    private String name;
    private String no;
    private String icon;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

}
